public class number_greater_than_100 {

    public static void main(String[] args) {

        int number=106;

        if(number>0){

            if (number>100){

                System.out.println("Number is greater than 100");

            }

            else {

                System.out.println("Number is less than 100");

            }

        }

        else{

            System.out.println("Invalid Input");

        }

    }

}
